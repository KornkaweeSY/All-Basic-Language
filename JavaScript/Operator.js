let a = 10, b = 5;

// เพิ่มค่าแบบ prefix
console.log("ค่าเริ่มต้น = ",a);
console.log("แบบ prefix =", ++a);
console.log("ค่าปัจจุบัน = ",a);
console.log("ค่าเริ่มต้น = ",b);
console.log("แบบ prefix =", --b);
console.log("ค่าปัจจุบัน = ",b);

a = 5, b = 15; //reset
// เพิ่มค่าแบบ postfix
console.log("ค่าเริ่มต้น = ",a);
console.log("แบบ postfix =", a++);
console.log("ค่าปัจจุบัน = ",a);
console.log("ค่าเริ่มต้น = ",b);
console.log("แบบ postfix =", b--);
console.log("ค่าปัจจุบัน = ",b);
